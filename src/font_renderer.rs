/// Newlines cause the cursor to move to the next line.
pub fn render_string(fb: &mut FrameBuffer, text: &str, start_x: u64, start_y: u64, fg: Color, bg: Color) {
    let mut x = start_x;
    let mut y = start_y;
    let max_x = fb.width();
    let max_y = fb.height();

    for byte in text.bytes() {
        match byte {
            b'\n' => {
                x = start_x;
                y += SCALED_CHAR_HEIGHT;
                if y + SCALED_CHAR_HEIGHT > max_y {
                    fb.scroll(SCALED_CHAR_HEIGHT);
                    y = max_y - SCALED_CHAR_HEIGHT;
                }
            }
            b'\r' => {
                x = start_x;
            }
            b'\t' => {
                // Tab = 4 character widths.
                x += SCALED_CHAR_WIDTH * 4;
                if x + SCALED_CHAR_WIDTH > max_x {
                    x = start_x;
                    y += SCALED_CHAR_HEIGHT;
                }
            }
            0x08 => {
                // Backspace.
                if x >= SCALED_CHAR_WIDTH {
                    x -= SCALED_CHAR_WIDTH;
                    render_char(fb, b' ', x, y, fg, bg);
                }
            }
            _ => {
                if x + SCALED_CHAR_WIDTH > max_x {
                    x = start_x;
                    y += SCALED_CHAR_HEIGHT;
                    if y + SCALED_CHAR_HEIGHT > max_y {
                        fb.scroll(SCALED_CHAR_HEIGHT);
                        y = max_y - SCALED_CHAR_HEIGHT;
                    }
                }
                render_char(fb, byte, x, y, fg, bg);
                x += SCALED_CHAR_WIDTH;
            }
        }
    }

    // Update cursor position.
    fb.set_cursor_position(x, y);
}

/// Render a test string with Hebrew characters.
pub fn render_hebrew_test(fb: &mut FrameBuffer) {
    let y = 50;
    render_string(fb, "exoterikOS - VESA Framebuffer Mode", 50, y, Color::WHITE, Color::BLACK);
    render_string(fb, "Hebrew: aleph-bet-gimel-dalet-hei", 50, y + SCALED_CHAR_HEIGHT * 2, Color::YELLOW, Color::BLACK);

    // Render actual Hebrew glyphs (code points 0xE0-0xE4).
    let hebrew_start_x = 200;
    let hebrew_y = y + SCALED_CHAR_HEIGHT * 4;
    let hebrew_bytes: &[u8] = &[0xE0, 0xE1, 0xE2, 0xE3, 0xE4];
    let mut x = hebrew_start_x;
    for &cp in hebrew_bytes {
        render_char(fb, cp, x, hebrew_y, Color::CYAN, Color::BLACK);
        x += SCALED_CHAR_WIDTH;
    }

    render_string(fb, "[Framebuffer Active]", 50, hebrew_y + SCALED_CHAR_HEIGHT * 2, Color::GREEN, Color::BLACK);
}

/// Calculate how many character columns fit in the framebuffer.
pub fn chars_per_line(fb: &FrameBuffer) -> u64 {
    fb.width() / SCALED_CHAR_WIDTH
}

/// Calculate how many character rows fit in the framebuffer.
pub fn lines_per_screen(fb: &FrameBuffer) -> u64 {
    fb.height() / SCALED_CHAR_HEIGHT
}
